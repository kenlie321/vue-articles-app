    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        //get
        $articles = Articles::orderBy('created_at','desc')->paginate(1);
        
        //return as resource
        return Article::collection($articles);
    }

