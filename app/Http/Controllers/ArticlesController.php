    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        //Get Article Detail
        $article = Articles::findOrFail($id);

        //return single article as resource
        if($article->delete()){
            return new Article($article);
        }
    }
